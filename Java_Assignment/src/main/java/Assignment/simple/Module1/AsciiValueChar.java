package Assignment.simple.Module1;

public class AsciiValueChar 
{ 
	public static void main(String[] args) 
	{
		char ch = 'A';
		int c = ch;
		System.out.println("Ascii value of character "+ch+ " is :" +c );
		
		
		// or use typecasting
		char cha ='A';
		System.out.println("Ascii value of character "+cha+ " is :" + (int)cha);
	}
	
	
}
